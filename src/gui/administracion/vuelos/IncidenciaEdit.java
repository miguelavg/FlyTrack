    private void llenarComboIncidencia() {
      ListatipoInci = CIncidencia.llenarComboEstado();;
      
      for (int i=0;i<ListatipoInci.size();i++)
        {
            Parametro TipoDocBE =(Parametro)ListatipoInci.get(i);
            
            cbm_incidencia.addItem(TipoDocBE);
        }
    }

